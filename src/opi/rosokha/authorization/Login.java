    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        if(isLogin()){
            if(user.getLogin().equals(User.ADMINISTRATOR)){
                frmMain.getUser().setStatus(User.ADMINISTRATOR);
            }else{
                frmMain.getUser().setStatus(User.REGISTERED_USER);
            }
            frmMain.checkUserStatus();
            this.setVisible(false);
        } else{
            JOptionPane.showMessageDialog(null, "Не вдалося увійти");
        }
    }//GEN-LAST:event_btnLoginActionPerformed


