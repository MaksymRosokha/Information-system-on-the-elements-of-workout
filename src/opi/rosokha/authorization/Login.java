    private boolean isLogin(){
        User u = ConnectorWithDatabase.login(txtLogin.getText(), Registration.hashPassword(txtPassword.getText()));
        if(u != null){
            user.setId(u.getId());
            user.setName(u.getName());
            user.setLogin(u.getLogin());
            user.setPassword(u.getPassword());
            return true;
        }
        return false;
    }
    
