    private void btnEditElemenеtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditElemenеtActionPerformed
        if(txtName.getText().length() > 255){
            showMessageDialog(null, "Назва елемемнту не повинна бути більшою за 255 символів" ,"Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(txtName.getText().length() < 3){
            showMessageDialog(null, "Назва елемемнту не повинна бути меншою за 3 символи" ,"Error",JOptionPane.ERROR_MESSAGE);
            return;
        }

        if(ConnectorWithDatabase.editElement(element.getId(),txtName.getText(), cmbType.getSelectedItem()+"", txtDescription.getText())){
            this.setVisible(false);
            showMessageDialog(null, "Елемент успішно відредагований");
        } else {
            showMessageDialog(null, "Не вдалося відредагувати елемент","Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditElemenеtActionPerformed


